    private void bt_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_editarActionPerformed
        try{
            //Instanciamos nuestro objeto
            innerObj  = new ClienteBO();

            ClienteBO cliente = (ClienteBO) cb_clientes.getSelectedItem();

            //Rescatamos los datos del formulario
            String nombre = tx_nombre.getText();
            String rut = tx_rut.getText();
            String direccion = tx_direccion.getText();
            String correo = tx_correo.getText();

            //Entregamos los datos al objeto
            innerObj.setId(cliente.getId());
            innerObj.setNombre(nombre);
            innerObj.setRut(rut);
            innerObj.setDireccion(direccion);
            innerObj.setCorreo(correo);

            //Guardamos el objeto
            ClienteDAO.update(innerObj);

            //Mostrar exito
            MostrarExito("El Cliente ha sido almacenado correctamente");

            LlenarListaClientes();
            MostrarCliente();
        }
        catch(ExceptionDAL ex){
            MostrarError(ex.getMessage());
        }
        catch(ExceptionBOL ex){
            MostrarError(ex.getMessage());
        }
    }//GEN-LAST:event_bt_editarActionPerformed

