    private void MostrarError(String mensaje){
        Color c = new Color(255, 0, 0);
        lb_mensaje.setForeground(c);
        lb_mensaje.setText(mensaje);
    }

    private void MostrarExito(String mensaje){
        Color c = new Color(102, 153, 0);
        lb_mensaje.setForeground(c);
        lb_mensaje.setText(mensaje);
    }

    private void LlenarListaClientes(){
        try{
            ArrayList<ClienteBO> lista = ClienteDAO.getAll();
            DefaultComboBoxModel datos = new DefaultComboBoxModel(lista.toArray());
            cb_clientes.setModel(datos);
        }
        catch(ExceptionDAL ex){
            MostrarError(ex.getMessage());
        }
    }

    private void BorrarMensaje(){
        lb_mensaje.setText("");
    }

    private void MostrarCliente(){
        ClienteBO cliente = (ClienteBO) cb_clientes.getSelectedItem();

        String nombre = cliente.getNombre();
        String rut = cliente.getRut();
        String direccion = cliente.getDireccion();
        String correo = cliente.getCorreo();

        //Rescatamos los datos del formulario
        tx_nombre.setText(nombre);
        tx_rut.setText(rut);
        tx_direccion.setText(direccion);
        tx_correo.setText(correo);

        bt_editar.setEnabled(false);
    }

