    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            //Instanciamos nuestro objeto
            innerObj  = new ClienteBO();

            //Rescatamos los datos del formulario
            String nombre = tx_nombre.getText();
            String rut = tx_rut.getText();
            String direccion = tx_direccion.getText();
            String correo = tx_correo.getText();

            //Entregamos los datos al objeto
            innerObj.setNombre(nombre);
            innerObj.setRut(rut);
            innerObj.setDireccion(direccion);
            innerObj.setCorreo(correo);

            //Guardamos el objeto
            ClienteDAO.save(innerObj);

            //Mostrar exito
            MostrarExito("El Clientes ha sido almacenado correctamente");
        }
        catch(ExceptionDAL ex){
            MostrarError(ex.getMessage());
        }
        catch(ExceptionBOL ex){
            MostrarError(ex.getMessage());
        }


    }//GEN-LAST:event_jButton1ActionPerformed

