    private void bt_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_agregarActionPerformed
        try{
            if(!innerObj.carroLleno()){
                ProductoBO producto = (ProductoBO) cb_productos.getSelectedItem();

                ItemVentaBO item = new ItemVentaBO(producto, 1);

                innerObj.agregarItem(item);
                tb_carro.setModel( new VentaUITableModel( innerObj.getCarro() ) );

                Actualizar();
            }
            else{
                throw new ExceptionBOL("El carro de venta esta lleno, no puede agragar m√°s productos");
            }
        }
        catch(ExceptionBOL ex){
            MostrarError(ex.getMessage());
        }
    }//GEN-LAST:event_bt_agregarActionPerformed

    private void bt_finalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_finalizarActionPerformed
        try{
            if(!innerObj.carroVacio()){
                ClienteBO cliente = (ClienteBO) cb_clientes.getSelectedItem();

                innerObj.setCliente(cliente);

                VentaDAO.save(innerObj);

                //Mostrar exito
                MostrarExito("La Venta ha sido almacenada correctamente");
            }
            else{
                throw new ExceptionBOL("El carro de venta no posee item, esta vacio");
            }
        }
        catch(ExceptionDAL ex){
            MostrarError(ex.getMessage());
        }
        catch(ExceptionBOL ex){
            MostrarError(ex.getMessage());
        }
    }//GEN-LAST:event_bt_finalizarActionPerformed

    private void tb_carroKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tb_carroKeyPressed
        int keycode = evt.getKeyCode();
        if(keycode == KeyEvent.VK_DELETE || keycode == KeyEvent.VK_BACK_SPACE){
            try{
                int[] filas = tb_carro.getSelectedRows();
                for(int i=0;i<filas.length;++i){
                    int id_producto =
                            Integer.parseInt(tb_carro.getModel().getValueAt(filas[i], 0).toString());
                    innerObj.removerItem(id_producto);
                }
            }

            catch(ExceptionBOL ex){
                MostrarError(ex.getMessage());
            }
            finally{
                tb_carro.setModel( new VentaUITableModel( innerObj.getCarro() ) );

            Actualizar();
            }
        }
    }//GEN-LAST:event_tb_carroKeyPressed

